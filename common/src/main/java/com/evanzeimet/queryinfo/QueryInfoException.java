package com.evanzeimet.queryinfo;

public class QueryInfoException extends Exception {

	private static final long serialVersionUID = 607631360051811795L;

	public QueryInfoException() {
		super();
	}

	public QueryInfoException(String message) {
		super(message);
	}

	public QueryInfoException(Throwable cause) {
		super(cause);
	}

	public QueryInfoException(String message, Throwable cause) {
		super(message, cause);
	}

}
